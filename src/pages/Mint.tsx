export default function Mint() {
  const [provider, setProvider] = useState<ethers.providers.Web3Provider | null>(null);
  const [signer, setSigner] = useState<ethers.Signer | null>(null);
  const [contract, setContract] = useState<ethers.Contract | null>(null);
  const [amount, setAmount] = useState<string>('');

  useEffect(() => {
    const init = async () => {
      const web3Modal = new Web3Modal();
      const providerInstance = await web3Modal.connect();
      const web3Provider = new ethers.providers.Web3Provider(providerInstance);
      const signer = web3Provider.getSigner();
      const contract = new ethers.Contract(contractAddress, contractABI, signer);

      setProvider(web3Provider);
      setSigner(signer);
      setContract(contract);
    };

    init();
  }, []);

  const mintTokens = async () => {
    if (contract && amount !== '') {
      try {
        const tx = await contract.mintFifty(ethers.parseUnits(amount, 18));
        await tx.wait();
        alert('Tokens minted successfully!');
      } catch (error) {
        console.error('Error minting tokens:', error);
        alert('Error minting tokens. See console for details.');
      }
    } else {
      alert('Contract or amount is not initialized.');
    }
  };

  return (
    <div className="min-h-screen flex flex-col items-center justify-center bg-gray-900 text-white p-4">
      <h1 className="text-4xl font-bold mb-8">Mint Theodores Tokens</h1>
      <input
        type="number"
        placeholder="Amount to mint"
        value={amount}
        onChange={(e) => setAmount(e.target.value)}
        className="mb-4 p-2 rounded text-black"
      />
      <button onClick={mintTokens} className="btn">
        Mint Tokens
      </button>
    </div>
  );
}

